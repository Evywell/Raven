<?php


namespace Test\Raven\Event;


use Raven\Framework\Event\Event;

class EntityEvent extends Event
{

    private $data;

    public function __construct($data)
    {
        $this->data = $data;
    }

    /**
     * @return mixed
     */
    public function getData()
    {
        return $this->data;
    }

    /**
     * @param mixed $data
     */
    public function setData($data)
    {
        $this->data = $data;
    }

}